# написать еще пару функций
def add(x, y):
    """ функция сложения двух чисел
    
    принимает два параметра и возвращает сумму 
    """
    return x + y
def octatoc(x, y):
    """ функция остаток от деления
    
    делит аргумент x по модулю y
    """
    return x % y
def divisionmod(x, y):
    """ функция Целочисленное деление
    
    Целочисленное деления аргумента x на аргумент y
    """
    return x // y


